import NextAuth from "next-auth";
import Adapters from "next-auth/adapters";
import { prisma } from "../../../utils.server";
import { authProviders } from "../../../config.server";
import { statService } from "../../../service/stat.service";

/**
 * LOCK 1: DYNAMIC URL INJECTION
 * This stops the [nextauth_url] warnings in your logs by 
 * telling the server exactly which domain it is currently on.
 */
const currentUrl = (() => {
  if (process.env.NEXTAUTH_URL) return process.env.NEXTAUTH_URL;
  if (process.env.VERCEL_URL) return `https://${process.env.VERCEL_URL}`;
  return "https://cusdis-jet-one.vercel.app";
})();

process.env.NEXTAUTH_URL = currentUrl;

export default NextAuth({
  providers: authProviders,
  adapter: Adapters.Prisma.Adapter({ prisma: prisma }),
  session: { jwt: true, maxAge: 30 * 24 * 60 * 60 },

  /**
   * LOCK 2: SECRET PERSISTENCE
   * This stops the "autogenerated key" warning. We use your c420... key
   * as a direct fallback so the server never has to "make one up."
   */
  jwt: {
    secret: process.env.NEXTAUTH_SECRET || 'c420d92ac78e5a0d8395920b9c425055ae911fe78b1fa97fc567dc6b4ec81b8f',
  },

  secret: process.env.NEXTAUTH_SECRET || 'c420d92ac78e5a0d8395920b9c425055ae911fe78b1fa97fc567dc6b4ec81b8f',

  debug: true,

  callbacks: {
    async jwt(token, user) {
      if (user) { token.id = user.id; }
      return token;
    },
    async session(session, userOrToken: any) {
      /**
       * LOCK 3: IDENTITY STICKINESS
       * This ensures session.uid is ALWAYS "DadAdmin" 
       */
      const id = (userOrToken?.id || userOrToken?.sub || userOrToken?.uid) as string;
      if (id) { session.uid = id; }
      console.log("NEXTAUTH DEBUG: Session UID set to:", session.uid);
      return session;
    },
    signIn() {
      statService.capture('signIn');
      return true;
    }
  }
});
